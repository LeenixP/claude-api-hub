const translations: Record<Locale, Record<string, string>> = { zh, en };

type TranslateFn = (key: string, params?: Record<string, string | number>) => string;

function createT(locale: Locale): TranslateFn {
  return (key: string, params?: Record<string, string | number>) => {
    let text = translations[locale][key] || translations['en'][key] || key;
    if (params) {
      for (const [k, v] of Object.entries(params)) {
        text = text.replace(`{${k}}`, String(v));
      }
    }
    return text;
  };
}

interface LocaleContextValue {
  locale: Locale;
  toggleLocale: () => void;
  t: TranslateFn;
}

const defaultLocale: Locale = (typeof localStorage !== 'undefined' && localStorage.getItem('locale') as Locale) || 'zh';

export const LocaleContext = createContext<LocaleContextValue>({
  locale: defaultLocale,
  toggleLocale: () => {},
  t: createT(defaultLocale),
});

export function useLocale(): LocaleContextValue {
  return useContext(LocaleContext);
}

export function useLocaleProvider(): LocaleContextValue {
  const [locale, setLocale] = useState<Locale>(
    () => (typeof localStorage !== 'undefined' && localStorage.getItem('locale') as Locale) || 'zh'
  );

  useEffect(() => {
    localStorage.setItem('locale', locale);
    document.documentElement.setAttribute('lang', locale);
  }, [locale]);

  const toggleLocale = useCallback(() => {
    setLocale(prev => prev === 'zh' ? 'en' : 'zh');
  }, []);

  const t = useCallback(createT(locale), [locale]);

  return { locale, toggleLocale, t };
}
